def _diff_bytes(got: bytes, expected: bytes, stride: int) -> str:
    lines = []
    for row in range(0, max(len(got), len(expected)), stride):
        g = got[row : row + stride]
        e = expected[row : row + stride]
        if g != e:
            lines.append(f"  row {row // stride}:")
            lines.append(f"    got:      {g.hex(' ')}")
            lines.append(f"    expected: {e.hex(' ')}")
    return "Tile data mismatch:\n" + "\n".join(lines)


